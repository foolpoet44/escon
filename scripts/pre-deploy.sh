#!/bin/bash

# ============================================================================
# Pre-Deployment Script for ESCON
#
# 배포 전 자동으로 실행되는 스크립트
# 용도: 데이터베이스 마이그레이션, 빌드 검증 등
#
# 실행: npm run pre-deploy
# ============================================================================

set -e  # 에러 발생 시 즉시 종료

echo "=================================================="
echo "🚀 배포 전 체크 시작"
echo "=================================================="

# ============================================================================
# Step 1: 환경 변수 검증
# ============================================================================

echo -e "\n📋 [Step 1] 환경 변수 검증..."

if [ -z "$SUPABASE_URL" ]; then
    echo "  ❌ 에러: SUPABASE_URL이 설정되지 않았습니다"
    echo "     Vercel 대시보드 → Project Settings → Environment Variables 에서 설정하세요"
    exit 1
fi

if [ -z "$SUPABASE_SERVICE_ROLE_KEY" ]; then
    echo "  ❌ 에러: SUPABASE_SERVICE_ROLE_KEY가 설정되지 않았습니다"
    exit 1
fi

echo "  ✅ Supabase 환경 변수 확인"

if [ -z "$LLM_API_KEY" ]; then
    echo "  ⚠️  경고: LLM_API_KEY가 설정되지 않았습니다 (선택사항)"
fi

# ============================================================================
# Step 2: Node.js 버전 확인
# ============================================================================

echo -e "\n📦 [Step 2] Node.js 버전 확인..."

NODE_VERSION=$(node -v)
echo "  ℹ️  Node.js: $NODE_VERSION"

# Node 18+ 필요
NODE_MAJOR=$(echo $NODE_VERSION | cut -d. -f1 | sed 's/v//')
if [ $NODE_MAJOR -lt 18 ]; then
    echo "  ❌ 에러: Node.js 18 이상이 필요합니다"
    exit 1
fi

echo "  ✅ Node.js 버전 OK"

# ============================================================================
# Step 3: 의존성 설치 및 검증
# ============================================================================

echo -e "\n📚 [Step 3] 의존성 확인..."

if [ ! -d "node_modules" ]; then
    echo "  🔄 npm install 실행 중..."
    npm install
fi

echo "  ✅ 의존성 설치 완료"

# ============================================================================
# Step 4: 타입 검사
# ============================================================================

echo -e "\n🔍 [Step 4] TypeScript 타입 검사..."

if ! npx tsc --noEmit 2>/dev/null; then
    echo "  ⚠️  경고: TypeScript 타입 에러가 있습니다 (배포는 계속됩니다)"
fi

echo "  ✅ 타입 검사 완료"

# ============================================================================
# Step 5: 빌드 검증
# ============================================================================

echo -e "\n🔨 [Step 5] 빌드 검증..."

if npm run build; then
    echo "  ✅ 빌드 성공"
else
    echo "  ❌ 빌드 실패: 배포를 중단합니다"
    exit 1
fi

# ============================================================================
# Step 6: 테스트 실행
# ============================================================================

echo -e "\n✅ [Step 6] 테스트 실행..."

if npm test -- --passWithNoTests 2>/dev/null; then
    echo "  ✅ 테스트 통과"
else
    echo "  ⚠️  경고: 일부 테스트가 실패했습니다 (배포는 계속됩니다)"
fi

# ============================================================================
# Step 7: 환경별 설정 준비
# ============================================================================

echo -e "\n⚙️  [Step 7] 환경 설정 준비..."

if [ "$VERCEL_ENV" = "production" ]; then
    echo "  📌 프로덕션 배포로 감지됨"
    echo "  ⚠️  주의: 프로덕션 데이터베이스에 영향을 미칩니다"
else
    echo "  📌 프리뷰 배포로 감지됨"
fi

echo "  ✅ 환경 설정 준비 완료"

# ============================================================================
# 완료
# ============================================================================

echo -e "\n=================================================="
echo "✨ 배포 전 체크 완료!"
echo "=================================================="
echo ""
echo "배포 정보:"
echo "  📌 환경: $VERCEL_ENV"
echo "  📌 Git SHA: ${VERCEL_GIT_COMMIT_SHA:0:7}"
echo "  📌 브랜치: ${VERCEL_GIT_BRANCH:-unknown}"
echo ""
echo "✅ 배포 준비 완료"
echo ""
